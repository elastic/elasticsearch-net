using System;
using System.Collections.Generic;

using Elastic.SourceGenerator.Roslyn;
using Elastic.SourceGenerator.Roslyn.Models;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace RequestConverter.SourceGenerator;

internal sealed partial class SourceFormatter
{
	private const string _prefix = "RC"; // TODO:

	public SourceProductionContext Context { get; }
	public ElasticsearchType Model { get; }

	public SourceFormatter(SourceProductionContext context, ElasticsearchType model)
	{
		Context = context;
		Model = model;
	}

	public static void GenerateSourceFiles(SourceProductionContext context, ElasticsearchType model)
	{
		var formatter = new SourceFormatter(context, model);
		formatter.AddAllSourceFiles();
	}

	private void AddAllSourceFiles()
	{
		Context.CancellationToken.ThrowIfCancellationRequested();

		var writer = new SourceWriter();
		StartFormatSourceFile(writer, Model.Type.Namespace, Model.Type.DeclarationHeaders);

		writer.WriteLine($$"""
		                  public void FormatToSource()
		                  {
		                  
		                  }
		                  """);

		EndFormatSourceFile(writer);

		AddSource("Constructor", writer.ToSourceText());
	}

	private void AddSource(string? suffix, SourceText source)
	{
		var hintName = $"{_prefix}" +
		               $"{((Model.Type.Namespace is not null) ? $"{Model.Type.Namespace}." : null)}" +
		               $"{Model.Type.Name}." +
		               $"{(!string.IsNullOrEmpty(suffix) ? $"{suffix}." : null)}" +
		               $"g.cs";

		Context.AddSource(hintName, source);
	}

	private static void StartFormatSourceFile(SourceWriter writer, string? ns, IEnumerable<string>? declarationHeaders)
	{
		writer.WriteLine("// <auto-generated/>");
		writer.WriteLine();

//#if DEBUG
//        writer.WriteLine("""
//                         #nullable enable

//                         """);
//#else
//        writer.WriteLine("""
//                         #nullable enable annotations
//                         #nullable disable warnings

//                         """);
//#endif
		
		if (ns is not null)
		{
			writer.WriteLine($"namespace {ns}");
			writer.WriteLine('{');
			writer.Indentation++;
		}

		foreach (var header in declarationHeaders ?? Array.Empty<string>())
		{
			writer.WriteLine(header);
			writer.WriteLine('{');
			writer.Indentation++;
		}
	}

	private static void EndFormatSourceFile(SourceWriter writer)
	{
		while (writer.Indentation > 0) 
		{
			writer.Indentation--;
			writer.WriteLine('}');
		}
	}
}
